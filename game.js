/* ----------------------------------------------------------------------------
   3b. NET  — optional multiplayer (served by serve.py). Degrades silently to solo.
   ----------------------------------------------------------------------------
   When serve.py is hosting the page, /net/info answers and we start trading ghost
   positions: POST our {x,y,…} every NET_SYNC_MS and draw everyone else's ghost
   (interpolated). Opened from file:// or a plain static server the probe just
   fails and this stays exactly the single-player game it has always been.
   -------------------------------------------------------------------------- */

function netRandomName() { return "Ghost-" + (1000 + Math.floor(Math.random() * 9000)); }

/** Probe for the relay; wire up identity + the (otherwise hidden) name field. */
function netInit() {
  net.name = netRandomName();
  if (mpNameEl) mpNameEl.value = net.name;
  if (!/^https?:$/.test(location.protocol)) return;   // file:// has no relay → stay solo
  net.base = location.origin;
  net.id = (window.crypto && crypto.randomUUID)
    ? crypto.randomUUID() : "g" + Math.random().toString(36).slice(2) + Date.now();
  try { net.room = new URLSearchParams(location.search).get("room") || "main"; } catch (e) { /* ignore */ }
  fetch(net.base + "/net/info", { cache: "no-store" })
    .then(r => r.ok ? r.json() : null)
    .then(info => { if (info && info.multiplayer) { net.on = true; if (mpNameWrap) mpNameWrap.classList.remove("hidden"); } })
    .catch(() => { /* no relay here — single-player, no noise */ });
}

/** Frame hook: pace the position exchange. @param {number} ts */
function netTick(ts) { if (net.on && !net.inflight && ts - net.lastSent >= NET_SYNC_MS) netSync(); }

/** One round-trip: send my ghost, receive everyone else's. */
function netSync() {
  net.inflight = true; net.lastSent = performance.now();
  net.name = (mpNameEl && mpNameEl.value.trim()) || net.name;
  const body = JSON.stringify({
    id: net.id, room: net.room, name: net.name, sheet: sheetIndex, msg: net.msg,
    x: +player.x.toFixed(3), y: +player.y.toFixed(3), fx: +player.fx.toFixed(3), fy: +player.fy.toFixed(3),
  });
  fetch(net.base + "/net/sync", { method: "POST", cache: "no-store", headers: { "Content-Type": "application/json" }, body })
    .then(r => r.ok ? r.json() : Promise.reject(r.status))
    .then(data => {
      net.fails = 0;
      const seen = new Set();
      for (const p of (data.players || [])) {
        seen.add(p.id);
        let peer = net.peers.get(p.id);
        if (!peer) { peer = { x: p.x, y: p.y, fx: p.fx, fy: p.fy, name: p.name, sheet: p.sheet }; net.peers.set(p.id, peer); }
        peer.tx = p.x; peer.ty = p.y; peer.fx = p.fx; peer.fy = p.fy; peer.name = p.name; peer.sheet = p.sheet; peer.msg = p.msg || "";
      }
      for (const id of [...net.peers.keys()]) if (!seen.has(id)) net.peers.delete(id);
    })
    .catch(() => { if (++net.fails > 25) { net.on = false; net.peers.clear(); } }) // relay vanished → solo
    .finally(() => { net.inflight = false; });
}

/** Ease each remote ghost toward its latest reported position (smooths ~8 Hz updates). */
function lerpPeers() {
  for (const peer of net.peers.values()) {
    if (peer.tx === undefined || peer.ty === undefined) continue;
    peer.x += (peer.tx - peer.x) * NET_LERP;
    peer.y += (peer.ty - peer.y) * NET_LERP;
  }
}

/** Draw one remote visitor's ghost + name tag. @param {Peer} peer */
function drawPeer(peer) {
  const c = toScreen(peer.x, peer.y);
  const n = SHEET_THEMES.length;
  const theme = SHEET_THEMES[((peer.sheet % n) + n) % n] || SHEET_THEMES[0];
  let sdx = peer.fx - peer.fy, sdy = (peer.fx + peer.fy) * 0.5;
  const sm = Math.hypot(sdx, sdy) || 1; sdx /= sm; sdy /= sm;
  const hover = Math.sin(tnow * 2.2 + (peer.x + peer.y)) * 2.0;
  shadow(c.x, c.y, 14);
  ctx.globalAlpha = 0.9;
  paintGhost(ctx, c.x, c.y - 8 + hover, 1, theme, sdx, sdy);
  ctx.globalAlpha = 1;
  if (peer.msg) speechBubble(peer.msg, c.x, c.y - 44 + hover);     // a posted message takes the place of the name tag
  else label(peer.name || "Ghost", c.x, c.y - 48 + hover, 12, "#cfe1ff");
}

