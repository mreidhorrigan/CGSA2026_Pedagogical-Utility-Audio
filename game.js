/** @param {KeyboardEvent} e */
function onKeyDown(e) {
  const k = e.key.toLowerCase();
  audio.resume();
  if (net.composing) return;            // composing a speech-bubble message — let the text input own the keys

  if (state === "intro") {
    if (!startBtn.disabled && ["enter", " ", "w", "a", "s", "d", "arrowup", "arrowdown", "arrowleft", "arrowright"].includes(k)) {
      e.preventDefault(); startGame();
    }
    return;
  }

  if (state === "slide") {
    // Leave by walking away: any movement key closes the slide and resumes
    // walking — so you never need Esc (which would drop the browser out of
    // fullscreen). E still toggles the slide shut, the same key that opened it.
    if (["w", "a", "s", "d", "arrowup", "arrowdown", "arrowleft", "arrowright"].includes(k)) {
      e.preventDefault();
      closeSlide();
      auto.active = false;                                   // manual movement takes over
      if (k === "w" || k === "arrowup") keys.up = true;      // start walking now; keyup clears it
      else if (k === "s" || k === "arrowdown") keys.down = true;
      else if (k === "a" || k === "arrowleft") keys.left = true;
      else if (k === "d" || k === "arrowright") keys.right = true;
      return;
    }
    if (k === "e" || k === "escape") { e.preventDefault(); closeSlide(); }                // E toggles shut (Esc still works, for non-fullscreen)
    else if (k === " ") { e.preventDefault(); closeSlide(); autoPathNext(); }             // Space → close + auto-walk to next
    else if (k === "," || k === "<") { e.preventDefault(); setSlide(currentSlide - 1); }  // browse: previous slide
    else if (k === "." || k === ">") { e.preventDefault(); setSlide(currentSlide + 1); }  // browse: next slide
    return;
  }

  // walking
  if (["arrowup", "arrowdown", "arrowleft", "arrowright", " "].includes(k)) e.preventDefault();
  if (k === " ") { autoPathNext(); return; }                                       // Space → auto-walk to next
  if (["w", "a", "s", "d", "arrowup", "arrowdown", "arrowleft", "arrowright"].includes(k)) auto.active = false; // manual takes over
  if (k === "w" || k === "arrowup") keys.up = true;
  else if (k === "s" || k === "arrowdown") keys.down = true;
  else if (k === "a" || k === "arrowleft") keys.left = true;
  else if (k === "d" || k === "arrowright") keys.right = true;
  else if (k === "e" || k === "enter") { if (activeIndex >= 0) openSlide(EXHIBITS[activeIndex].slide, activeIndex); }
  else if (k === "c") { sheetIndex = (sheetIndex + 1) % SHEET_THEMES.length; sfx.sheet(); toast(`Sheetghost ${sheetIndex + 1} of ${SHEET_THEMES.length}`); }
  else if (k === "m") { audio.setMuted(!audio.muted); toast(audio.muted ? "Sound off" : "Sound on"); }
  else if (k === "f") { e.preventDefault(); toggleMessage(); }   // post / clear a speech-bubble message (preventDefault so this "f" doesn't leak into the composer it opens)
}

/** @param {KeyboardEvent} e */
function onKeyUp(e) {
  const k = e.key.toLowerCase();
  if (k === "w" || k === "arrowup") keys.up = false;
  else if (k === "s" || k === "arrowdown") keys.down = false;
  else if (k === "a" || k === "arrowleft") keys.left = false;
  else if (k === "d" || k === "arrowright") keys.right = false;
}

/** Tap / click in the play area (touch, pen, or mouse): walk to a tapped kiosk and
 *  open it, otherwise walk to the tapped floor tile. Mirrors the Space auto-walk, so
 *  a phone visitor needs no keyboard. @param {PointerEvent} e */
function onCanvasPointer(e) {
  if (state !== "walking" || net.composing) return;   // only while roaming; ignore taps mid-slide / while typing
  if (e.pointerType === "mouse" && e.button > 0) return; // ignore non-primary MOUSE buttons; always allow touch / pen
  e.preventDefault();
  audio.ensure(); audio.resume();                      // first touch also unlocks audio on mobile
  const r = canvas.getBoundingClientRect();
  const sx = e.clientX - r.left, sy = e.clientY - r.top;
  const ki = kioskAtScreen(sx, sy);
  if (ki >= 0) walkToKiosk(ki);
  else { const w = screenToWorld(sx, sy); walkToPoint(w.x, w.y); }
}

/** Begin auto-walking to a kiosk; it opens once we're in range. @param {number} i exhibit index */
function walkToKiosk(i) {
  auto.active = true; auto.goal = i;
  auto.lastDist = Infinity; auto.stuck = 0;
}

/** Begin auto-walking to a free floor point (tapped tile); we just stop on arrival.
 *  @param {number} tx @param {number} ty */
function walkToPoint(tx, ty) {
  auto.active = true; auto.goal = -1;
  auto.tx = clamp(tx, 0.4, GRID - 1 - 0.4);
  auto.ty = clamp(ty, 0.4, GRID - 1 - 0.4);
  auto.lastDist = Infinity; auto.stuck = 0;
}

/** Auto-walk to (and then open) the next slide; first slide if none reached yet.
 *  This is the hook a future "audio-instruction" navigator would call. */
function autoPathNext() {
  if (!EXHIBITS.length) return;
  let t = currentTarget < 0 ? 0 : currentTarget + 1;
  if (t >= EXHIBITS.length) t = 0;          // wrap around
  walkToKiosk(t);
}

