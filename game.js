/** @returns {Exhibit|null} nearest kiosk not yet visited */
function nextUnvisited() {
  let best = Infinity; /** @type {Exhibit|null} */ let pick = null;
  for (const e of EXHIBITS) {
    if (e.visited) continue;
    const d = Math.hypot(player.x - e.tx, player.y - e.ty);
    if (d < best) { best = d; pick = e; }
  }
  return pick;
}

