/** @param {number} slideIdx @param {number} [exIdx] */
function openSlide(slideIdx, exIdx) {
  currentSlide = clamp(slideIdx, 0, SLIDES.length - 1);
  if (exIdx !== undefined && exIdx >= 0) { EXHIBITS[exIdx].visited = true; currentTarget = exIdx; }
  sfx.interact(exIdx !== undefined && exIdx >= 0 ? exIdx : currentSlide);
  keys.up = keys.down = keys.left = keys.right = false;
  state = "slide";
  renderSlideDOM();
  slideEl.classList.remove("hidden");
  slideCardEl.focus();
}

function closeSlide() {
  sfx.close();
  slideEl.classList.add("hidden");
  slideStageEl.innerHTML = "";          // unload any iframe/PDF
  state = "walking";
  last = performance.now();
  try { canvas.focus(); } catch (_) { /* ignore */ }  // reclaim the keyboard from the slide iframe so movement + keyup reach the game
}

/** @param {number} i */
function setSlide(i) {
  const ni = clamp(i, 0, SLIDES.length - 1);
  if (ni !== currentSlide) sfx.nav();
  currentSlide = ni;
  renderSlideDOM();
}

function renderSlideDOM() {
  const s = SLIDES[currentSlide];
  const type = s.type || (s.html ? "html" : s.src ? guessType(s.src) : "html");
  slideTitleEl.textContent = s.title || `Slide ${currentSlide + 1}`;
  slideCounterEl.textContent = `Slide ${currentSlide + 1} of ${SLIDES.length}`;
  slideStageEl.className = "slide-stage " + type;

  if (type === "image") {
    slideStageEl.innerHTML = `<img class="slide-img" src="${escapeAttr(s.src || "")}" alt="${escapeAttr(s.title)}">`;
  } else if (type === "embed") {
    if (s.src) {
      const url = toEmbedUrl(s.src);
      slideStageEl.innerHTML = `<iframe class="slide-embed" src="${escapeAttr(url)}" title="${escapeAttr(s.title)}"`
        + ` allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share; fullscreen"`
        + ` referrerpolicy="strict-origin-when-cross-origin" allowfullscreen></iframe>`;
    } else {                            // a pasted <iframe…> embed snippet (presenter-authored)
      slideStageEl.innerHTML = s.html || "";
    }
  } else if (type === "pdf") {
    slideStageEl.innerHTML = `<iframe class="slide-doc" src="${escapeAttr(s.src || "")}#view=FitH" title="${escapeAttr(s.title)}"></iframe>`;
  } else if (s.html) {                // html inline: authored, placeholder, or preloaded (instant)
    slideStageEl.innerHTML = `<iframe class="slide-doc" srcdoc="${escapeAttr(s.html)}" title="${escapeAttr(s.title)}"></iframe>`;
  } else {                            // html from a file (not preloaded — e.g. file://)
    slideStageEl.innerHTML = `<iframe class="slide-doc" src="${escapeAttr(s.src || "")}" title="${escapeAttr(s.title)}"></iframe>`;
  }
  wireSlideIframeKeys();              // let E / Esc / WASD leave the slide even once the iframe owns the keyboard
}

/** A slide iframe (HTML deck, PDF) grabs the keyboard once it's focused — and the
 *  game's keydown listener is on the *parent* window, which iframe key events never
 *  reach. For SAME-ORIGIN iframes we forward just the keys the deck itself doesn't
 *  use — E/Esc (leave) and WASD (walk away) — back to the game; arrows / Space /
 *  PageUp-Down / Home / End / F stay with the deck's own navigator. Cross-origin
 *  iframes (PDF viewer, video embeds) can't be reached, so there the ✕ button leaves. */
function wireSlideIframeKeys() {
  const ifr = /** @type {HTMLIFrameElement|null} */ (slideStageEl.querySelector("iframe"));
  if (!ifr) return;
  ifr.addEventListener("load", () => {
    let doc = null;
    try { doc = ifr.contentDocument; } catch (_) { doc = null; }   // cross-origin → blocked
    if (doc) doc.addEventListener("keydown", onSlideIframeKey);
  });
}

/** Forward a leave/walk-away keypress made *inside* a same-origin slide iframe.
 *  @param {KeyboardEvent} e */
function onSlideIframeKey(e) {
  if (state !== "slide") return;
  const k = (e.key || "").toLowerCase();
  if (k === "e" || k === "escape") { e.preventDefault(); closeSlide(); return; }
  if (k === "w" || k === "a" || k === "s" || k === "d") {        // walk away to leave
    e.preventDefault();
    auto.active = false;
    if (k === "w") keys.up = true;
    else if (k === "s") keys.down = true;
    else if (k === "a") keys.left = true;
    else if (k === "d") keys.right = true;
    closeSlide();                                                // focuses the canvas → the matching keyup reaches the game
  }
  // arrows / Space / PageUp-Down / Home / End / F intentionally left to the deck.
}

