/** Build the room for `count` slides: size the grid, place kiosks on a ring,
 *  lay the walkway, and centre the player. @param {number} count */
function buildRoom(count) {
  const n = Math.max(1, count);
  const R = Math.max(3.4, (n * KIOSK_SPACING) / (2 * Math.PI)); // ring radius grows with n
  const margin = 2.5;
  GRID = Math.max(9, Math.ceil(2 * (R + margin)));
  const cx = (GRID - 1) / 2, cy = (GRID - 1) / 2;

  EXHIBITS = [];
  for (let i = 0; i < n; i++) {
    const ang = -Math.PI / 2 + (i * 2 * Math.PI) / n;       // start at top, clockwise
    EXHIBITS.push({
      tx: clamp(Math.round(cx + R * Math.cos(ang)), 1, GRID - 2),
      ty: clamp(Math.round(cy + R * Math.sin(ang)), 1, GRID - 2),
      title: SLIDES[i] ? SLIDES[i].title : `Slide ${i + 1}`,
      color: KIOSK_PALETTE[i % KIOSK_PALETTE.length],
      slide: i,
      visited: false,
    });
  }
  separateExhibits();

  pathTiles.clear();
  for (let i = 0; i < EXHIBITS.length; i++) line(EXHIBITS[i], EXHIBITS[(i + 1) % EXHIBITS.length]);

  player.x = cx; player.y = cy; player.fx = 0; player.fy = 1; player.moving = false;
  activeIndex = prevActive = currentTarget = -1;
  auto.active = false; auto.goal = -1;
}

/** Nudge any kiosks that rounded onto the same tile. */
function separateExhibits() {
  const seen = new Set();
  for (const e of EXHIBITS) {
    let key = e.tx + "," + e.ty, guard = 0;
    while (seen.has(key) && guard++ < 12) {
      e.ty = clamp(e.ty + 1, 1, GRID - 2);
      if (e.ty >= GRID - 2) { e.ty = 1; e.tx = clamp(e.tx + 1, 1, GRID - 2); }
      key = e.tx + "," + e.ty;
    }
    seen.add(key);
  }
}

/** @param {Exhibit} a @param {Exhibit} b — add an L-shaped walkway between two kiosks */
function line(a, b) {
  let x = a.tx, y = a.ty;
  while (x !== b.tx) { pathTiles.add(x + "," + y); x += Math.sign(b.tx - x); }
  while (y !== b.ty) { pathTiles.add(x + "," + y); y += Math.sign(b.ty - y); }
  pathTiles.add(b.tx + "," + b.ty);
}

