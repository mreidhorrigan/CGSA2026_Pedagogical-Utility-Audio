/** @param {number} wx @param {number} wy */
function isoToScreen(wx, wy) { return { x: (wx - wy) * (TILE_W / 2), y: (wx + wy) * (TILE_H / 2) }; }
/** @param {number} wx @param {number} wy */
function toScreen(wx, wy) { const p = isoToScreen(wx, wy); return { x: p.x + originX, y: p.y + originY }; }

/** Inverse of toScreen: a canvas-relative screen point → world tile coords on the
 *  floor plane (used to turn a tap/click into a walk goal). @param {number} sx @param {number} sy */
function screenToWorld(sx, sy) {
  const ix = sx - originX, iy = sy - originY;          // solve isoToScreen for (wx,wy)
  return { x: ix / TILE_W + iy / TILE_H, y: iy / TILE_H - ix / TILE_W };
}

/** Which kiosk (if any) was tapped: a screen-space hit-box spanning each kiosk's
 *  pillar + sign (taller than its floor tile, so tapping the visible sign counts);
 *  the front-most wins where boxes overlap. @param {number} sx @param {number} sy @returns {number} exhibit index or -1 */
function kioskAtScreen(sx, sy) {
  let pick = -1, bestDepth = -Infinity;
  for (let i = 0; i < EXHIBITS.length; i++) {
    const c = toScreen(EXHIBITS[i].tx, EXHIBITS[i].ty);
    if (sx >= c.x - 34 && sx <= c.x + 34 && sy >= c.y - 96 && sy <= c.y + 18) {
      const depth = EXHIBITS[i].tx + EXHIBITS[i].ty;
      if (depth > bestDepth) { bestDepth = depth; pick = i; }
    }
  }
  return pick;
}

/** @param {number} cx @param {number} cy @param {string} fill @param {string|null} stroke */
function diamond(cx, cy, fill, stroke) {
  ctx.beginPath();
  ctx.moveTo(cx, cy - TILE_H / 2); ctx.lineTo(cx + TILE_W / 2, cy);
  ctx.lineTo(cx, cy + TILE_H / 2); ctx.lineTo(cx - TILE_W / 2, cy);
  ctx.closePath();
  ctx.fillStyle = fill; ctx.fill();
  if (stroke) { ctx.strokeStyle = stroke; ctx.lineWidth = 1; ctx.stroke(); }
}

/** @param {number[][]} pts @param {string} fill */
function poly(pts, fill) {
  ctx.beginPath(); ctx.moveTo(pts[0][0], pts[0][1]);
  for (let i = 1; i < pts.length; i++) ctx.lineTo(pts[i][0], pts[i][1]);
  ctx.closePath(); ctx.fillStyle = fill; ctx.fill();
}

/** @param {number} x @param {number} y @param {number} w @param {number} h @param {number} r @param {string|null} fill @param {string|null} stroke */
function roundRect(x, y, w, h, r, fill, stroke) {
  ctx.beginPath();
  ctx.moveTo(x + r, y);
  ctx.arcTo(x + w, y, x + w, y + h, r); ctx.arcTo(x + w, y + h, x, y + h, r);
  ctx.arcTo(x, y + h, x, y, r); ctx.arcTo(x, y, x + w, y, r);
  ctx.closePath();
  if (fill) { ctx.fillStyle = fill; ctx.fill(); }
  if (stroke) { ctx.strokeStyle = stroke; ctx.lineWidth = 2; ctx.stroke(); }
}

/** @param {number} cx @param {number} cy @param {number} rx */
function shadow(cx, cy, rx) {
  ctx.save(); ctx.fillStyle = "rgba(0,0,0,0.30)";
  ctx.beginPath(); ctx.ellipse(cx, cy, rx, rx * 0.5, 0, 0, Math.PI * 2); ctx.fill();
  ctx.restore();
}

/** @param {string} text @param {number} x @param {number} y @param {number} size @param {string} color */
function label(text, x, y, size, color) {
  ctx.font = `700 ${size}px -apple-system,Segoe UI,Roboto,sans-serif`;
  ctx.textAlign = "center"; ctx.textBaseline = "alphabetic";
  ctx.lineWidth = 4; ctx.strokeStyle = "rgba(0,0,0,0.6)"; ctx.strokeText(text, x, y);
  ctx.fillStyle = color; ctx.fillText(text, x, y);
}

/** A small speech bubble of `text` whose tail tip sits at (cx, tipY). Canvas text
 *  only — never HTML — so a remote message can't inject markup. Word-wraps to a few
 *  short lines and ellipsises anything that still overflows. @param {string} text */
function speechBubble(text, cx, tipY) {
  const fontSize = 13, lineH = 16, padX = 9, padY = 6, maxTextW = 200, maxLines = 3;
  ctx.font = `600 ${fontSize}px -apple-system,Segoe UI,Roboto,sans-serif`;
  // greedy word-wrap, then clamp to maxLines
  const all = [];
  let line = "";
  for (const w of String(text).split(" ")) {
    const test = line ? line + " " + w : w;
    if (ctx.measureText(test).width > maxTextW && line) { all.push(line); line = w; }
    else line = test;
  }
  if (line) all.push(line);
  const lines = all.slice(0, maxLines);
  if (all.length > maxLines && lines.length) lines[lines.length - 1] += " …";
  for (let i = 0; i < lines.length; i++) {                 // clamp any single over-long line (e.g. one huge word)
    let s = lines[i];
    if (ctx.measureText(s).width > maxTextW) {
      while (s.length > 1 && ctx.measureText(s + "…").width > maxTextW) s = s.slice(0, -1);
      lines[i] = s + "…";
    }
  }
  if (!lines.length) return;
  const tw = Math.min(maxTextW, Math.max.apply(null, lines.map(l => ctx.measureText(l).width)));
  const bw = tw + padX * 2, bh = lines.length * lineH + padY * 2;
  const bx = cx - bw / 2, by = tipY - 7 - bh;             // bubble sits above a 7px tail
  roundRect(bx, by, bw, bh, 9, "rgba(8,12,24,0.92)", "rgba(95,232,255,0.9)");
  ctx.beginPath();                                        // downward tail toward the ghost
  ctx.moveTo(cx - 6, by + bh - 0.5); ctx.lineTo(cx + 6, by + bh - 0.5); ctx.lineTo(cx, by + bh + 7);
  ctx.closePath(); ctx.fillStyle = "rgba(8,12,24,0.92)"; ctx.fill();
  ctx.fillStyle = "#eaf6ff"; ctx.textAlign = "center"; ctx.textBaseline = "middle";
  for (let i = 0; i < lines.length; i++) ctx.fillText(lines[i], cx, by + padY + lineH / 2 + i * lineH);
}

/** Lighten (amt>0) / darken (amt<0) a #rrggbb colour. @param {string} hex @param {number} amt */
function shade(hex, amt) {
  const n = parseInt(hex.slice(1), 16);
  let r = (n >> 16) & 255, g = (n >> 8) & 255, b = n & 255;
  const f = amt < 0 ? 0 : 255, p = Math.abs(amt);
  r = Math.round(r + (f - r) * p); g = Math.round(g + (f - g) * p); b = Math.round(b + (f - b) * p);
  return `rgb(${r},${g},${b})`;
}

/** @param {string} hex @param {number} a */
function hexA(hex, a) { const n = parseInt(hex.slice(1), 16); return `rgba(${(n >> 16) & 255},${(n >> 8) & 255},${n & 255},${a})`; }

/** @param {number} v @param {number} lo @param {number} hi */
function clamp(v, lo, hi) { return v < lo ? lo : v > hi ? hi : v; }

/** Escape for element text. @param {string} s */
function escapeHtml(s) { return String(s).replace(/[&<>"]/g, ch => ({ "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;" }[ch] || ch)); }
/** Escape for a double-quoted attribute (keeps inner markup intact, e.g. srcdoc). @param {string} s */
function escapeAttr(s) { return String(s).replace(/&/g, "&amp;").replace(/"/g, "&quot;"); }

