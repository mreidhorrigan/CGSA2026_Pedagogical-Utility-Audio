/** @param {number} dt seconds */
function update(dt) {
  // desired movement direction: an auto-walk target (a kiosk to open, or a tapped
  // floor point to stop at), else the held keys.
  let dx = 0, dy = 0;
  if (auto.active) {
    const toKiosk = auto.goal >= 0 && auto.goal < EXHIBITS.length;
    const g = toKiosk ? EXHIBITS[auto.goal] : null;
    const gx = (g ? g.tx : auto.tx) - player.x, gy = (g ? g.ty : auto.ty) - player.y;
    const d = Math.hypot(gx, gy);
    const arrive = toKiosk ? INTERACT_RANGE * 0.9 : 0.3;  // 0.3 > max step/frame → no overshoot jitter at a tapped point
    if (d <= arrive) {                                    // arrived
      auto.active = false;
      if (g) { openSlide(g.slide, auto.goal); return; }   // kiosk → modal now open
    } else {
      if (d > auto.lastDist - 1e-3) auto.stuck += dt; else auto.stuck = 0; // blocked (e.g. tapped behind a kiosk)?
      auto.lastDist = d;
      if (auto.stuck > 0.7) auto.active = false;          // give up rather than grind in place
      else { dx = gx; dy = gy; }
    }
  }
  if (!auto.active) {
    if (keys.up)    { dx -= 1; dy -= 1; }
    if (keys.down)  { dx += 1; dy += 1; }
    if (keys.left)  { dx -= 1; dy += 1; }
    if (keys.right) { dx += 1; dy -= 1; }
  }

  player.moving = (dx !== 0 || dy !== 0);
  if (player.moving) {
    const m = Math.hypot(dx, dy);
    dx /= m; dy /= m;
    player.fx = dx; player.fy = dy;
    let nx = player.x + dx * PLAYER_SPEED * dt;
    let ny = player.y + dy * PLAYER_SPEED * dt;

    for (const ex of EXHIBITS) {                          // gentle circular collision
      let ox = nx - ex.tx, oy = ny - ex.ty;
      const d = Math.hypot(ox, oy);
      if (d < OBSTACLE_RADIUS) {
        if (d < 1e-4) { ox = 1; oy = 0; }
        nx = ex.tx + (ox / (d || 1)) * OBSTACLE_RADIUS;
        ny = ex.ty + (oy / (d || 1)) * OBSTACLE_RADIUS;
      }
    }
    const margin = 0.4;
    player.x = clamp(nx, margin, GRID - 1 - margin);
    player.y = clamp(ny, margin, GRID - 1 - margin);
  }

  activeIndex = -1;
  let best = INTERACT_RANGE;
  for (let i = 0; i < EXHIBITS.length; i++) {
    const d = Math.hypot(player.x - EXHIBITS[i].tx, player.y - EXHIBITS[i].ty);
    if (d < best) { best = d; activeIndex = i; }
  }
  if (activeIndex !== prevActive) {
    if (activeIndex >= 0) sfx.enter(activeIndex);
    prevActive = activeIndex;
  }
}

